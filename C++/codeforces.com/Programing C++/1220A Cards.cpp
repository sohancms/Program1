#include <iostream>
#include <algorithm>
using namespace std;

int main()
{

    int n,i;
    cin >> n;
    char s[n];
    cin >> s;
    sort(s,s+n);
    for(i=0; i<n; i++)
    {
        if(s[i]=='n')
            cout << 1 << " ";
        if(s[i]=='r')
            cout << 0 << " ";

    }

return 0;
}
