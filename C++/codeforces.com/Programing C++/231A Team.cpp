#include <iostream>
using namespace std;

int main()
{
    int n,i,j,p,count2=0;
    cin >> n;
    for(i=0;i<n;i++)
    {
        int count1=0;
        for(j=0;j<=2;j++)
        {
            cin >> p;
            if(p==1)
            {
                count1++;
            }
        }
        if(count1>=2)
        {
            count2++;
        }
    }

    cout << count2;
return 0;
}
