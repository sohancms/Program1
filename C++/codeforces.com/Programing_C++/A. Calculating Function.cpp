#include <bits/stdc++.h>
using namespace std;

int main()
{
    long long n, ans=0;
    cin >> n;
    if ( n % 2 == 0 )
    {
        ans=n/2;
    }
    else
    {
        ans=-1*(n+1)/2;
    }
    cout << ans << endl;
return 0;
}
